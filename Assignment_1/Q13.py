# Create two sets {1, 2, 3} and {3, 4, 5}. Print their union.

# Define the sets
set1 = {1, 2, 3}
set2 = {3, 4, 5}

# Get the union of the sets
union_set = set1.union(set2)

# Print the result
print("Union of sets:", union_set)